T = int(input())
for tc in range(1, T+1):
    N = int(input())
    arr = [list(map(int, input().split())) for _ in range(N)]
    max_square = 0
    for r in range(N):
        for c in range(N):
            if arr[r][c] == 1:
                sr, sc = r, c
                width, height = 0, 0
                while sc < N and arr[r][sc] == 1:
                    sc += 1
                    width += 1
                while sr < N and arr[sr][c] == 1:
                    sr += 1
                    height += 1
                square = width * height
                if max_square < square:
                    max_square = square
                for rr in range(r, sr):
                    for cc in range(c, sc):
                        arr[rr][cc] = 0
    print(f"#{tc} {max_square}")
